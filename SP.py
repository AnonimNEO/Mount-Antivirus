            def create_checkboxes(self):
                programs = list(PROGRAM_INFO.keys())
                num_programs = len(programs)
                half_programs = (num_programs + 1) // 2 #–†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º —Å–µ—Ä–µ–¥–∏–Ω—É, –æ–∫—Ä—É–≥–ª—è—è –≤–≤–µ—Ä—Ö

                for i in range(num_programs):
                    program = programs[i]
                    var = tk.BooleanVar(value=False)
                    checkbox = ttk.Checkbutton(self.scrollable_frame, text=program, variable=var)

                    #–†–∞–∑–º–µ—â–µ–Ω–∏–µ —á–µ–∫–±–æ–∫—Å–æ–≤ –≤ –¥–≤–∞ —Å—Ç–æ–ª–∞
                    if i < half_programs:
                        checkbox.grid(row=i, column=0, sticky="w", padx=1, pady=2)
                    else:
                        checkbox.grid(row=i - half_programs, column=1, sticky="w", padx=10, pady=2)

                    self.checkbox_vars[program] = var
                    self.checkboxes[program] = checkbox



            def create_path(self, path):
                try:
                    os.makedirs(path, exist_ok=True)
                    logger.info(f"SP - –°–æ–∑–¥–∞–Ω –∫–∞—Ç–∞–ª–æ–≥: {path}")
                except OSError as e:
                    logger.error(f"SP - –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ {path}:\n{e}")



            def create_file(self, path):
                try:
                    with open(path, "w"):
                        pass #–°–æ–∑–¥–∞—ë–º –ø—É—Å—Ç–æ–π —Ñ–∞–π–ª
                    logger.info(f"SP - –°–æ–∑–¥–∞–Ω —Ñ–∞–π–ª: {path}")
                except OSError as e:
                    logger.error(f"SP - –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —Ñ–∞–π–ª–∞ {path}:\n{e}")



            def create_registry_key(self, key_path, value_name, value_data, value_type=winreg.REG_SZ):
               try:
                    with winreg.CreateKeyEx(winreg.HKEY_LOCAL_MACHINE, key_path, 0, winreg.KEY_ALL_ACCESS) as key:
                        winreg.SetValueEx(key, value_name, 0, value_type, value_data)
                    logger.info(f"SP - –°–æ–∑–¥–∞–Ω –∫–ª—é—á —Ä–µ–µ—Å—Ç—Ä–∞: {key_path}\\{value_name} = {value_data}")
               except OSError as e:
                    logger.error(f"SP - –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∫–ª—é—á–∞ —Ä–µ–µ—Å—Ç—Ä–∞ {key_path}\\{value_name} = {value_data}:\n{e}")



            def delete_path(self, path):
                try:
                    if os.path.exists(path):
                        shutil.rmtree(path)
                        logger.info(f"SP - –£–¥–∞–ª–µ–Ω–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è: {path}")
                except OSError as e:
                    logger.error(f"SP - –û—à–∏–±–∫–∞ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ {path}:\n{e}")



            def delete_file(self, path):
                try:
                    if os.path.exists(path):
                        os.remove(path)
                        logger.info(f"SP - –£–¥–∞–ª–µ–Ω —Ñ–∞–π–ª: {path}")
                except OSError as e:
                    logger.error(f"SP - –û—à–∏–±–∫–∞ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ —Ñ–∞–π–ª–∞ {path}:\n{e}")



            def delete_registry_key(self, key_path, value_name):
                try:
                    with winreg.OpenKeyEx(winreg.HKEY_LOCAL_MACHINE, key_path, 0, winreg.KEY_ALL_ACCESS) as key:
                        winreg.DeleteValue(key, value_name)
                    logger.info(f"SP - –£–¥–∞–ª–µ–Ω –∫–ª—é—á —Ä–µ–µ—Å—Ç—Ä–∞: {key_path}\\{value_name}")
                except FileNotFoundError:
                    logger.info(f"SP - –ö–ª—é—á —Ä–µ–µ—Å—Ç—Ä–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω: {key_path}\\{value_name}")
                except OSError as e:
                    logger.error(f"SP - –û—à–∏–±–∫–∞ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ –∫–ª—é—á–∞ —Ä–µ–µ—Å—Ç—Ä–∞ {key_path}\\{value_name}:\n{e}")



            def run_simulation(self):
                for program, info in PROGRAM_INFO.items():
                    if self.checkbox_vars[program].get():
                        logger.info(f"SP - –ó–∞–ø—É—Å–∫ —Å–∏–º—É–ª—è—Ü–∏–∏ –¥–ª—è {program}")
                        if "path" in info:
                            for path in info["path"]:
                                self.create_path(path)
                        if "files" in info:
                            for file_path in info["files"]:
                                self.create_file(file_path)
                        if "registry_keys" in info:
                            for key_path, key_values in info["registry_keys"].items():
                                for value_name, value_data in key_values.items():
                                    self.create_registry_key(key_path, value_name, value_data)
                        logger.info(f"SP - –°–∏–º—É–ª—è—Ü–∏—è –¥–ª—è {program} –∑–∞–≤–µ—Ä—à–µ–Ω–∞")
                messagebox.showinfo(random_string(), "–°–∏–º—É–ª—è—Ü–∏—è –∑–∞–≤–µ—Ä—à–µ–Ω–∞.")



